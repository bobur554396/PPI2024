#include <iostream>

using namespace std;

int main(){
    // - [ ] (++, --, +=, -=, *=, /=, %= )

    int a = 2;

    // a = a + 4; // 6
    // a = a * 2; // 12

    a += 4; // a = a + 4; // 6
    a *= 2; // a = a * 2; // 12
    a %= 5; // a = a % 5; // 2

    // Unary Operator ++, --

    int b = 3;

    b++; // b = b + 1; <=> b += 1; 4
    b--; // b = b - 1; <=> b -= 1; 3


    
    
    

    


    return 0;
}