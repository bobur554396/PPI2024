#include <iostream>

using namespace std;

int main(){
    // - [ ] comparison operators (==, !=, >, <, >=, <=)

    bool ok = (4 == 4);

    cout << ok << endl;
    cout << (2 > 3) << endl;
    cout << (2 < 3) << endl;
    cout << (2 == 3) << endl; // are those number equal?
    cout << (2 != 3) << endl; // are those number not equal?

    return 0;
}